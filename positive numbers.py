# Python program to print positive Numbers in a List 
# list of numbers
list1 = [12, -7, 5, 64, -14]
list2 = [12, 14, -95, 3]

print("Output List1:")
for num in list1:
    if num >= 0:
       print(num, end = " \n")
       
print("Output List2:")
for num in list2:
    if num >= 0:
       print(num, end = " \n")
